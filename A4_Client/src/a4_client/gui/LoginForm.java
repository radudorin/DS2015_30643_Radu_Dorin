    private void showClientForm(User user) {
        if (user.isAdmin()) {
            return;
        }
        ClientForm clientForm = new ClientForm(user);
        clientForm.setVisible(true);
        closeCurrentFrame();
    }

    private void showAdminForm(User user) {
        if (!user.isAdmin()) {
            return;
        }
        AdminForm adminForm = new AdminForm(user.getId());
        adminForm.setVisible(true);
        closeCurrentFrame();
    }

    private void closeCurrentFrame() {
        setVisible(false);
        registerForm.setVisible(false);
        dispose();
    }

    private void login(String username, String password) {
        User user = clientProxy.login(username, password);

        if (user != null) {
            if (user.isAdmin()) {
                showAdminForm(user);
            } else {
                showClientForm(user);
            }
        } else {
            errorText.setVisible(true);
            errorText.setText("Please verify your username and password and try again.");
        }
    }

    private void register(User user) {

        if (user == null) {
            return;
        }

        int id = clientProxy.register(user);
        if (id == -1) {
            registerErrorLabel.setVisible(true);
            registerErrorLabel.setText("There was a problem registering you. Please try again.");
            return;
        } else {
            user.setId(id);
        }

        if (user.isAdmin()) {
            showAdminForm(user);
        } else {
            showClientForm(user);
        }

    }

