    private void registerDoneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerDoneButtonActionPerformed
        if (registerUsernameField.getText().equals("")) {
            registerErrorLabel.setVisible(true);
            registerErrorLabel.setText("Enter an username");
            return;
        }

        if (registerPasswordField1.getText().equals("")) {
            registerErrorLabel.setText("Enter a valid password");
            return;
        }

        if (registerPasswordField2.getText().equals("")) {
            registerErrorLabel.setText("Enter a valid password");
            return;
        }

        if (registerFullNameField.getText().equals("")) {
            registerErrorLabel.setText("Enter your full name");
            return;
        }

        if (!registerPasswordField2.getText().equals(registerPasswordField1.getText())) {
            registerErrorLabel.setText("Your passwords do not match");
            return;
        }

        String username = registerUsernameField.getText();
        String password = registerPasswordField1.getText();
        String fullName = registerFullNameField.getText();

        User user = new User();
        user.setUsername(username);
        user.setPassword(password);
        user.setFullName(fullName);

        register(user);
    }//GEN-LAST:event_registerDoneButtonActionPerformed

